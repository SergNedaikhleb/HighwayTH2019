package lesson6;

public class IncrementDecrementExample {

    public static void main(String[] args) {
        int a=1;

        System.out.println("original value "+a);
        System.out.println("autoincrement "+ a++);
        System.out.println("original value "+a);
        System.out.println("original value "+a);
        System.out.println("original value "+a);
    }
}
